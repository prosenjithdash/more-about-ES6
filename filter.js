// Filter selects elements based on a condition and returns a array with the elements that fulfilled the condition.

const players = [69, 45, 50, 78, 62, 90];
const splayer = players.filter(f => f > 60)
console.log(splayer)

const splayer2 = players.filter(f => f > 100)
console.log(splayer2)

const splayer3 = players.filter(f => f > 20)
console.log(splayer3)